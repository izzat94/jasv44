     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, array(

            'status' => 'required',
            'renew' => 'required'

            ));

        //store in the database
        $ietscp = new IetsCp;

        $ietscp->status = $request->status;
        $ietscp->renew = $request->renew;

        $ietscp -> save();

        Session::flash('success', 'The Competent Person Profile has been saved!');

        return redirect() -> route(('ietscp.show'),$ietscp->id);
    }

